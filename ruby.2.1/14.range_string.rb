#!/usr/bin/env ruby
# encoding: utf-8

# a, b, c, d
puts 'alphabets'
for alphabet in 'a'..'d'
  puts alphabet
end

# $, %, &
puts 'symbols'
for symbol in '$'..'&'
  puts symbol
end

# 0, 1, 2, 3
puts 'numbers'
for number in '0'..'3'
  puts number
end

# あ, ぃ, い, ぅ, う
puts 'hiragana'
for hiragana in 'あ'..'う'
  puts hiragana
end
